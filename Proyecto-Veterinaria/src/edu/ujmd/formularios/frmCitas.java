    public void llenarComboClientes() {
        try {
            litsCliente = controlcli.findClientesEntities();
            for (int i = 0; i < litsCliente.size(); i++) {
                enticli = (Clientes) litsCliente.get(i);
                cboCliente.addItem(enticli.getNombre());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos : " + e.getMessage());
        }
    }

    public void llenarComboEmpleados() {
        try {
            litsEmpleado = controlemp.findEmpleadosEntities();
            for (int i = 0; i < litsEmpleado.size(); i++) {
                entiem = (Empleados) litsEmpleado.get(i);
                cboEmpleado.addItem(entiem.getNombre());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos : " + e.getMessage());
        }
    }

    public int buscarComboClientes(String depto) {
        int index = 0;
        try {
            for (int i = 0; i < litsCliente.size(); i++) {
                enticli = (Clientes) litsCliente.get(i);
                if (enticli.getCodClientes().equals(depto)) {
                    index = i;
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos : " + e.getMessage());
        }
        return index;
    }

    public int buscarComboEmpleados(String dep) {
        int index = 0;
        try {
            for (int i = 0; i < litsEmpleado.size(); i++) {
                entiem = (Empleados) litsEmpleado.get(i);
                if (entiem.getCodEmpleados().equals(dep)) {
                    index = i;
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos : " + e.getMessage());
        }
        return index;
    }

