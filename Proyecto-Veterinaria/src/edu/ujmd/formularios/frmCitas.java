    public void cargar() {
        entici.setFechaHora(this.calendarcita.getDate());
        enticli.setCodClientes(cboCliente.getSelectedIndex() + 1);
        entici.setCodClientes(enticli);
        entiem.setCodEmpleados(cboEmpleado.getSelectedIndex() + 1);
        entici.setCodEmpleados(entiem);

    }

    public void limpiar() {
        this.lblcodigo.setText("Generado Automaticamente");
        this.cboCliente.setSelectedIndex(0);
        this.cboEmpleado.setSelectedIndex(0);
    }

    public void llenarTabla() {
        String col[] = {"ID", "CLIENTE", "EMPLEADO", "FECHA"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0);
        Object[] obj = new Object[5];
        List ls;

        try {
            ls = controlcit.findCitasEntities();
            for (int i = 0; i < ls.size(); i++) {
                entici = (Citas) ls.get(i);
                obj[0] = entici.getCodCitas();
                obj[1] = entici.getCodClientes().getNombre();
                obj[2] = entici.getCodEmpleados().getNombre();
                obj[3] = entici.getFechaHora();

                modelo.addRow(obj);
            }
            this.tablacitas.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos : " + e.getMessage());
        }

    }

